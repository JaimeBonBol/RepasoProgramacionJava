package CondicionalesyBucles.BuclesAnidados;

public class Ejercicio2 {
    public static void main(String[] args) {
        int i, j;

        for (i=1; i<=3; i++){
            j=i+1;
            while (j<4){
                System.out.println(j-1);
                j=j+1;
            }
        }
    }
}
