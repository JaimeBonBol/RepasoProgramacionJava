package CondicionalesyBucles;

public class Uso_Bucle_For {
    public static void main(String[] args) {
        for (int i=0; i<10; i++){
            System.out.println("Hola");
        }
        System.out.println();
        for (int i = 10; i >0; i--) {
            System.out.println("hola");
        }
    }
}
